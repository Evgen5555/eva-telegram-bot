function getBlock(direction, step, answer) {
  const custom = MICRO[direction]?.custom?.[step];
  if (custom) return custom;

  const block = MICRO[direction]?.[step]?.[answer];
  return block || DEFAULT;
}

function buildFollowUp(direction, step, answer, nextQuestion) {
  const { reaction, insight } = getBlock(direction, step, answer);
  return `${reaction}\n\n${insight}\n\n${nextQuestion}`;
}

function buildMicroOnly(direction, step, answer) {
  const { reaction, insight } = getBlock(direction, step, answer);
  return `${reaction}\n\n${insight}`;
}

